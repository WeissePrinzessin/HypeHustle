package HelpClass;

public class SampleTask {
    private
    String name, author, description, time;
    int price, rating, IDauthor, IDperformer;

    public String getName() {return name;}
    public String getAuthor() {return author;}
    public String getDescription() {return description;}
    public String getTime() {return time;}
    public int getPrice() {return price;}
    public int getRating() {return rating;}
    public int getIDauthor() {return IDauthor;}
    public int getIDperformer() {return IDperformer;}

    public void setName(String name) {this.name = name;}
    public void setAuthor(String author) {this.author = author;}
    public void setDescription(String description) {this.description = description;}
    public void setTime(String time) {this.time = time;}
    public void setPrice(int price) {this.price = price;}
    public void setRating(int rating) {this.rating = rating;}
    public void setIDauthor(int IDauthor) {this.IDauthor = IDauthor;}
    public void setIDperformer(int IDperformer) {this.IDperformer = IDperformer;}

}
